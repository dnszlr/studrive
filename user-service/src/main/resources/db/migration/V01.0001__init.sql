CREATE SCHEMA IF NOT EXISTS userservice;

CREATE TABLE IF NOT EXISTS userservice.users (
    ID SERIAL PRIMARY KEY,
    FIRSTNAME VARCHAR(50) NULL,
    LASTNAME VARCHAR(50) NULL,
    EMAIL VARCHAR(225) NULL,
    PAYMENTDETAILID BIGINT,
    UNIVERSITY VARCHAR(225)
);

CREATE TABLE IF NOT EXISTS userservice.paymentDetails (
    ID SERIAL PRIMARY KEY,
    COUNTRYCODE VARCHAR(2) NULL,
    CHECKDIGIT INTEGER NULL,
    BANKCODE INTEGER NULL,
    ACCOUNTNUMBER INTEGER NULL
);